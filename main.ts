scene.onHitWall(SpriteKind.Enemy, function (sprite, location) {
    if (sprite.isHittingTile(CollisionDirection.Left)) {
        sprite.vx = 50
    }
    if (sprite.isHittingTile(CollisionDirection.Right)) {
        sprite.vx = -50
    }
})
